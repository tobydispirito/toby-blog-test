@app.route('/register', methods=["POST", "GET"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            flash(message="There is already a user associated with that email address, please login.")
            return redirect(url_for('login'))
        else:
            new_user = User(
                email=form.email.data,
                password=generate_password_hash(password=form.password.data, method=HASH_METHOD, salt_length=8),
                name=form.name.data
            )
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect(url_for('get_all_posts'))

    return render_template("register.html", form=form)


@app.route('/login', methods=["POST", "GET"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            user_in_question = User.query.filter_by(email=form.email.data).first()
            if check_password_hash(pwhash=user_in_question.password, password=form.password.data):
                login_user(user_in_question)
                print(f"Current user ID is: {current_user.id}")
                return redirect(url_for('get_all_posts'))
            else:
                flash(message="The password does not match the one on record")
                return redirect(url_for('login'))
        else:
            flash(message="There is no account associated with that email address")
            return redirect(url_for('login'))

    return render_template("login.html", form=form)


